function yamlEscape(s) {
  if (/[:#\n"|]/.test(s)) return JSON.stringify(s);
  return s;
}

function yamlList(arr) {
  return arr.map((x) => `  - ${yamlEscape(x)}`).join('\n');
}

function yamlFaqs(faqs) {
  return faqs
    .map(
      (f) =>
        `  - question: ${yamlEscape(f.question)}\n    answer: ${yamlEscape(f.answer)}`,
    )
    .join('\n');
}

